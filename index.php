<script>
    const carousel = document.querySelector('.testimonial-carousel');
const arrowPrev = document.querySelector('.arrow-prev');
const arrowNext = document.querySelector('.arrow-next');

let currentPosition = 0;

arrowPrev.addEventListener('click', () => {
    currentPosition += carousel.offsetWidth;
    if (currentPosition > 0){
        currentPosition = 0;
    }
    carousel.scrollTo(currentPosition, 0);
});

arrowNext.addEventListener('click', () => {
    const carouselWidth = carousel.offsetWidth;
    const maxScrollPosition = carousel.scrollWidth - carousel.clientWidth;
    if (currentPosition < maxScrollPosition - carouselWidth) {
        currentPosition -= carouselWidth;
    }
    carousel.scrollTo(currentPosition, 0);
});

carousel.addEventListener('scroll', () => {
    currentPosition = carousel.scrollLeft;
});
